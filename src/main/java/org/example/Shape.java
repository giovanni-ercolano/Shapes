package org.example;

public class CPoint{

    private double Area = 0;
    private double Perimeter = 0;

    public CPoint() {
    }

    public double getArea() {
        return Area;
    }

    public void setArea(double area) {
        Area = area;
    }

    public double getPerimeter() {
        return Perimeter;
    }

    public void setPerimeter(double perimeter) {
        Perimeter = perimeter;
    }
}
